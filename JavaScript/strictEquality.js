// = assignment operator
// == comparison operator
// === strict equality operator (compare if calues & datatype are equal)
// != inequality operator
// !== strict inequality operator

const PI = 3.14;

if(PI === "3.14"){ //if we only use the comparison operator it will return "true" since the values are technically the same
    console.log("That is Pi")
}
else{
    console.log("That is NOT Pi")
}

if(PI !== "3.14"){ //if we only use the comparison operator it will return "true" since the values are technically the same
    console.log("That is NOT Pi")
}
else{
    console.log("That is Pi")
}